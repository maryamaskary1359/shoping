export const wordToPersian = (word) => {
  switch (word) {
    case 'printed':
      return 'کتاب و مجله'
    case 'music':
      return 'آلات موسقی'
    case 'carpet':
      return 'فرش دست باف و تابلو فرش'
    case 'handicrafts':
      return 'صانیع دستی'
    case 'book':
      return 'کتاب چاپی'
    case 'magazine':
      return 'مجلات'
    case 'guitar':
      return 'گیتار'
    case 'violin':
      return 'ویولین'
    case 'instrument':
      return 'ساز های ایرانی'
    case 'panel':
      return 'تابلو فرش'
    case 'leather':
      return 'کیف چرمی'
    case 'Enamels':
      return 'میناکاری'
    case 'turquoise':
      return 'فیروزه کوبی'
    
    default:
      return '*'
  }
}
